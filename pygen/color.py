# Exit codes available for print customization
class Color:
    BLUE:       str = "\033[94m"
    BOLD:       str = "\033[1m"
    CYAN:       str = "\033[96m"
    END:        str = "\033[0m"
    GREEN:      str = "\033[92m"
    PURPLE:     str = "\033[95m"
    RED:        str = "\033[91m"
    UNDERLINED: str = "\033[4m"
    YELLOW:     str = "\033[93m"
